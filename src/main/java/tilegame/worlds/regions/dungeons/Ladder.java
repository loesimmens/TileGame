package tilegame.worlds.regions.dungeons;

public class Ladder {
    private int xUp;
    private int xDown;
    private int yUp;
    private int yDown;
    private int z;

    public int getxUp() {
        return xUp;
    }

    public void setxUp(int xUp) {
        this.xUp = xUp;
    }

    public int getxDown() {
        return xDown;
    }

    public void setxDown(int xDown) {
        this.xDown = xDown;
    }

    public int getyUp() {
        return yUp;
    }

    public void setyUp(int yUp) {
        this.yUp = yUp;
    }

    public int getyDown() {
        return yDown;
    }

    public void setyDown(int yDown) {
        this.yDown = yDown;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
